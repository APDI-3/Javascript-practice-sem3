let day=parseInt(prompt('enter a number between 1-5'))
switch(day){
    case 1:
        console.log('saturday')
        break
    case 2:
        console.log('sunday')
        break
    case 3:
        console.log('monday')
        break
    case 4:
        console.log('tuesday')
        break
    case 5:
        console.log('wednesday')
        break
    default:
        console.log('invalid data try again')
}